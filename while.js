/* Executa se o teste for verdadeiro */
var c = 1
while(c <= 6){
    console.log(`passo ${c}`)
    c++
}
console.log('Nova contagem..')
/* Executa, depois faz o teste lógico */
var c2 = 1
do{
    console.log(`passo ${c2}`)
    c2++
}while(c2 <= 6)
/* Estrura de repetição com teste lógica e incremento */
for (var c3 = 1; c3 <= 6; c3++){
    console.log(`${c3}`)
}